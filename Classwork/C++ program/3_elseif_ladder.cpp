#include<iostream>
using namespace std;
int main()
{
    int num;
    cout<<"enter number";
    cin>>num;

    if(num==10)
    {
        cout<<"10"<<endl;
    }
    else if(num==20)
    {
        cout<<"20"<<endl;
    }
    else if(num==50)
    {
        cout<<"50"<<endl;
    }
    else
    {
        cout<<"not valid"<<endl;
    }
}
